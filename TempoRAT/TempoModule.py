#Imports.
import os

def whoami():
    os.system("whoami")

def dir():
    os.system("dir")

def ip():
    os.system("ipconfig")

def clear():
    os.system("cls")

def tasklist():
    os.system("tasklist")

def shutdown():
    os.system("shutdown -s")

def restart():
    os.system("shutdown -r")